#include "../inc/fractol.h"

void	color(t_window *data, int i)
{
	if (i == 0)
		data->color = 0x00aaff;
	if (i == 1)
		data->color = 0xff9900;
	if (i == 2)
	{
		data->color = 0xff0000;
		data->colorvalue = 0;
	}
}
